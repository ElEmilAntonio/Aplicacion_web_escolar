       <P></P>
Un vaso de precipitado es un recipiente cilndrico de vidrio borosilicatado fino que se utiliza muy comnmente en el laboratorio, sobre todo, para preparar o calentar sustancias, medir o traspasar lquidos. Es cilndrico con un fondo plano; se le encuentra de varias capacidades, desde 100 mL hasta de varios litros. Normalmente es de vidrio, de metal o de un plstico en especial y es aquel cuyo objetivo es contener gases o lquidos. Tiene componentes de tefln u otros materiales resistentes a la corrosin.
