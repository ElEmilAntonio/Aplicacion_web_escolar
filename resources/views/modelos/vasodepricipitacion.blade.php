Su objetivo principal es contener lquidos o sustancias qumicas diversas de distinto tipo.
Como su nombre lo dice permite obtener precipitados a partir de la reaccin de otras sustancias.
Normalmente es utilizado para trasportar lquidos a otros recipientes.
Tambin se puede utilizar para calentar, disolver, o preparar reacciones qumicas.
