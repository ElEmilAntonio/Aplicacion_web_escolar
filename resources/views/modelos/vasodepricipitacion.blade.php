Son de vidrio y de plstico (Cuando estn hechos de vidrio se utiliza un tipo de material mucho ms resistente que el convencional denominado pyrex).
Posee componentes de tefln y otros materiales resistentes a la corrosin.
Su capacidad vara desde el mililitro hasta el litro (o incluso ms).
